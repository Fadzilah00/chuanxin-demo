// Global reactive state for instant updates
const globalLocale = ref('en')

export const useI18n = () => {
  // Initialize locale properly
  const getInitialLocale = () => {
    if (typeof window !== 'undefined') {
      const savedLocale = localStorage.getItem('chuanxin-locale')
      return (savedLocale && translations[savedLocale]) ? savedLocale : 'en'
    }
    return 'en'
  }
  
  // Initialize global state only once
  if (typeof window !== 'undefined' && globalLocale.value === 'en') {
    const initialLocale = getInitialLocale()
    if (initialLocale !== 'en') {
      globalLocale.value = initialLocale
    }
  }
  
  // Create instant reactive translation function
  const t = (key) => {
    const keys = key.split('.')
    let value = translations[globalLocale.value]
    
    for (const k of keys) {
      value = value?.[k]
    }
    
    return value || key
  }
  
  // Instant setLocale - no delays, no events, just direct update
  const setLocale = (newLocale) => {
    if (globalLocale.value !== newLocale && translations[newLocale]) {
      // Instant update
      globalLocale.value = newLocale
      
      // Save to localStorage
      if (typeof window !== 'undefined') {
        localStorage.setItem('chuanxin-locale', newLocale)
      }
    }
  }
  
  // Create reactive translations object
  const currentTranslations = computed(() => translations[globalLocale.value])
  
  return {
    locale: globalLocale,
    t,
    setLocale,
    currentTranslations,
    availableLocales: Object.keys(translations)
  }
}